-- CAMPO IDENTITY (TIPO DO CAMPO AUTO INCREMENTO)
-- *QUANDO EXECUTAMOS O COMANDO INSERT NÃO É NECESSÁRIO ESPECIFICAR O CAMPO IDENTITY
-- SÓ PODEMOS TER 1 CAMPO IDENTITY POR TABELA
-- PODEMOS FORÇAR O VALOR DO AUTO INCREMENTO DURANTE O INSERT. MAS DEVEMOS TER CUIDADO COM ISSO

CREATE TABLE TAB_IDENTITY
(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY
    ,DESCRITOR VARCHAR(20)
    ,PRIMARY KEY (ID)
);

